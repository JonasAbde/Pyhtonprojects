# Exercise - For Loop

# 1. Create a for loop that prints out the numbers 0-9
print("Numbers 0-9:")
for i in range(10):
    print(i)

print("\nNumbers 1-10:")
# 2. Create a for loop that prints out the numbers 1-10
for i in range(1, 11):
    print(i)

print("\nEvery other number from 0-100:")
# 3. Create a for loop that prints out every other number from 0-100
for i in range(0, 101, 2):
    print(i)

print("\nNumbers from 200 to 100 in steps of 3:")
# 4. Create a for loop that prints out the numbers from 200 to 100 in steps of 3
for i in range(200, 99, -3):
    print(i)
